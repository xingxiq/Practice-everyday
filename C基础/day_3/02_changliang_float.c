#include "stdio.h"

int main()
{
    float a = 3.1415926;
    printf("a = %f\n",a);
    float b = 45.8676;
    printf("b = %f\n",b);
    double c = 3456.2345666;
    printf("c = %lf\n",c);
    float d = 3456789777;
    printf("d = %e\n",d);
    float e = 0.000056785;
    printf("e = %e\n",e);
    return 0;
}
